package hr.dream.factory.learning.models;

public class Rune {
    int runeRank;
    String name;
    Boolean highRune;

    public int getRuneRank() {
        return runeRank;
    }

    public void setRuneRank(int runeRank) {
        this.runeRank = runeRank;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getHighRune() {
        return highRune;
    }

    public void setHighRune(Boolean highRune) {
        this.highRune = highRune;
    }
}
